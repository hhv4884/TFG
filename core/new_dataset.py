    def __init__(self, dataset, classes):
        self.dataset = dataset
        self.classes = classes
        self.map_old_to_new = {}
        newD = {}
        # remapeo
        mapping = {}
        for i in range(len(classes)):
            mapping[classes[i]] = i
        # print(mapping) -> {0: 0, 1: 7}
        index = 0
        for datapoint in dataset:
            image, label = datapoint
            if label in classes:
                newLabel = mapping.get(label)
                newD[index] = (image, newLabel)
                index += 1
        self.newD = newD

    def __len__(self):
        return len(self.newD)

    def __getitem__(self, i):
        return self.newD[i]

